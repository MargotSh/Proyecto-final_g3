def operaciones_basicas(a, b):
    print(f"Suma: {a + b}")
    print(f"Resta: {a - b}")
    print(f"Multiplicación: {a * b}")
    print(f"División: {a / b}")
    print(f"Potencia: {a ** b}")

# Ejemplo de uso
a = int(input("Ingrese el primer número: "))
b = int(input("Ingrese el segundo número: "))
operaciones_basicas(a, b)